// Function to get coordinates for a pincode
export function getCoordinatesForPincode(pincode) {
  const coords = pincodeCoordinates[pincode];
  if (coords) {
    return coords;
  }
  
  // If exact pincode not found, try to find a nearby pincode in same area
  // by matching first 3 digits (postal circle/district)
  const prefix = pincode.substring(0, 3);
  const nearbyPincodes = Object.keys(pincodeCoordinates).filter(p => p.startsWith(prefix));
  
  if (nearbyPincodes.length > 0) {
    // Return first matching pincode coordinates as approximation
    return pincodeCoordinates[nearbyPincodes[0]];
  }
  
  return null;
}

// Function to validate if pincode exists in our database
export function isPincodeSupported(pincode) {
  return pincodeCoordinates.hasOwnProperty(pincode);
}

export default pincodeCoordinates;
